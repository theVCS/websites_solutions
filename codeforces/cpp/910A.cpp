void solve()
{
    int n, d;
    string s;

    cin >> n >> d;
    cin >> s;

    int i = 0;
    int cnt = 0;

    while (true)
    {
        int j = min(i + d, n - 1);

        while (s[j] == '0')
        {
            j--;
        }
        
        if(j == i)
        {
            cout << -1 << endl;
            return;
        }

        i = j;
        cnt++;

        if(i == n - 1)
        {
            cout << cnt;
            return;
        }
    }
}

