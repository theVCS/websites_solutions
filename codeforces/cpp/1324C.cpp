void solve()
{
    string s;
    vector<int> index;

    cin >> s;
    index.push_back(0);
    for (int i = 0; i < s.size(); i++)
    {
        if (s[i] == 'R')
            index.push_back(i + 1);
    }
    index.push_back(s.size() + 1);

    int res = 0;

    for (int i = 0; i < index.size() - 1; i++)
    {
        res = max(res, index[i + 1] - index[i]);
    }
    cout << res << endl;
}

