ll ans;

void add(int index)
{
    if (arr[index] > arr[index - 1] && arr[index] > arr[index + 1])
        ans += arr[index];
    if (arr[index] < arr[index - 1] && arr[index] < arr[index + 1])
        ans -= arr[index];
}

void remove(int index)
{
    if (arr[index] > arr[index - 1] && arr[index] > arr[index + 1])
        ans -= arr[index];
    if (arr[index] < arr[index - 1] && arr[index] < arr[index + 1])
        ans += arr[index];
}

