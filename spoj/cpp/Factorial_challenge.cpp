double fun(ll n)
{
    double d = n * log(n) - n + log(2 * pi * n) / 2;
    return x * log(10) - d;
}

void solve()
{
    cin >> x;
    ll ans = 1;
    ll start = 1, end = 1e12;

    while (start <= end)
    {
        ll mid = (start + end) / 2;
        double v = fun(mid);
        if (v >= 0)
            ans = max(ans, mid), start = mid + 1;
        else
            end = mid - 1;
    }

    cout << ans << endl;

    // cout<<log(2.718281828459045)<<endl;
}

